#include <iostream>

using namespace std;

int main(int argc, char * argv[])
{
	int a = 10, b = 5, c = 3;
	
	cout << "a + b = " << a + b << endl;
	cout << "a - b = " << a - b << endl;
	cout << "a * b = " << a * b << endl;
	cout << "a / b = " << a / b << endl;
	cout << "a % b = " << a % b << endl;
	cout << "a % c = " << a % c << endl << endl;

	int i = 0;
	cout << "++i = " << ++i << endl;
	i = 0;
	cout << "i++ = " << i++ << endl;
	i = 0;
	cout << "--i = " << --i << endl;
	i = 0;
	cout << "i-- = " << i-- << endl;
}

// a + b = 15
// a - b = 5
// a * b = 50
// a / b = 2
// a % b = 0
// a % c = 1

